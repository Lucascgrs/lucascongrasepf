    private void ValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValiderActionPerformed
        // TODO add your handling code here:
        int C1 = c1.getSelectedIndex();
        int C2 = c2.getSelectedIndex();
        progresstime.setMinimum(0);
        progresstime.setMaximum(20);
        progresstime.setValue(0);
        pg1.setMinimum(0);
        pg1.setMaximum(100);
        pg1.setValue(50);
        pg2.setMinimum(0);
        pg2.setMaximum(100);
        pg2.setValue(50);
        c1.setBackground(Color.gray);
        p1.setBackground(Color.yellow);
        c2.setBackground(Color.gray);
        p2.setBackground(Color.yellow);
        double cc = 0;
        double S = Double.parseDouble(somme.getText());
        boolean v = false;
        Random generateurAleat = new Random();
        
        switch (C1){
            case 1:
                if (cc != 0){
                    cc *= 2;
                }else{
                    cc = 2;
                }
                break;
            case 2:
                if (cc != 0){
                    cc *= 1.5;
                }else{
                    cc = 1.5;
                }
                break;
        }
        
        switch (C2){
            case 1:
                if (cc != 0){
                    cc *= 1.4;
                }else{
                    cc = 1.4;
                }
                break;
            case 2:
                if (cc != 0){
                    cc *= 1.2;
                }else{
                    cc = 1.2;
                }
                break;
        }
                
        cote.setText("Cote : " + cc);
        double Money = Double.parseDouble(Argent.getText());
        if (Money - S >= 0 & S > 0 & cc > 0 & v == false){
            Argent.setText((Money - S) + "");
            v = true;
        
            Timer timer = new Timer();
            timer.schedule(new TimerTask(){
                int n1;
                int n2;
                int time = 20;
                boolean P1 = false;
                boolean P2 = false;
                double c = Double.parseDouble(cote.getText().split(" ")[2]);
                double s = Double.parseDouble(somme.getText());
                @Override
                public void run(){
                    if (time == 0){
                        int fp1 = pg1.getValue();
                        int fp2 = pg2.getValue();
                        if((fp1 <= 50 & C1 == 1) | (fp1 >= 50 & C1 == 2) | (C1 == 0)){
                            P1 = true;
                            c1.setBackground(Color.green);
                            p1.setBackground(Color.green);
                        }else{
                            c1.setBackground(Color.red);
                            p1.setBackground(Color.red);
                        }
                        
                        if((fp2 <= 50 & C2 == 1) | (fp2 >= 50 & C2 == 2) | (C2 == 0)){
                            P2 = true;
                            c2.setBackground(Color.green);
                            p2.setBackground(Color.green);
                        }else{
                            c2.setBackground(Color.red);
                            p2.setBackground(Color.red);
                        }
                        
                        if (P1 & P2){
                            res.setText((s * c) + "");
                            Argent.setText((Double.parseDouble(Argent.getText()) + (s * c)) + "");
                        }
                        
                        cancel();
                    }
                    
                    progresstime.setValue(time);
                    jLabel15.setText("Temps Restant : " + time);
                    n1 = generateurAleat.nextInt(3);
                    n2 = generateurAleat.nextInt(3);
                    switch(n1){
                        case 0:
                            if (pg1.getValue() - 5 >= 0){
                                pg1.setValue(pg1.getValue() - 5);
                                break;
                            }
                        case 1:
                            if (pg1.getValue() + 5 <= 100){
                                pg1.setValue(pg1.getValue() + 5);
                                break;
                            }
                    }
                    
                    switch(n2){
                        case 0:
                            if (pg2.getValue() - 5 >= 0){
                                pg2.setValue(pg2.getValue() - 5);
                                break;
                            }
                        case 1:
                            if (pg2.getValue() + 5 <= 100){
                                pg2.setValue(pg2.getValue() + 5);
                                break;
                            }
                    }
                    time--;
                    }

                }, 100, 1000);
        }
    }//GEN-LAST:event_ValiderActionPerformed

