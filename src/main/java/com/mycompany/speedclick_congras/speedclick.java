    public void timergame(){
        Timer timer = new Timer();
        timer.schedule(new TimerTask(){
            int time = Time;
            @Override
            public void run(){
                if (time == 0){
                    newgame.setEnabled(true);
                    jouer = false;
                    if(cpt > Integer.parseInt(meilleurscore.getText())){
                        meilleurscore.setText(cpt + "");
                    }
                    cancel();
                }
                //System.out.println(time);
                chrono.setText(time + "");
                progresstemps.setValue(time);
                time--;
            }
        }, 1000, 1000);
    }

